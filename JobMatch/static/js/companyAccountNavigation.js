
switch (window.location.pathname) {
	case '/company-account/':
		$('#account-link').addClass('active')
		break
	case '/job-posts/':
		$('#job-posts-link').addClass('active')
		break
	case '/job-posts/new/':
		$('#new-job-post-link').addClass('active')
		break
	case '/notifications/':
		$('#notifications-link').addClass('active')
		break
}





